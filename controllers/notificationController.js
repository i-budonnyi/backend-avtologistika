const pool = require("../config/db");

// üîê –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —Ç–æ–∫–µ–Ω–∞ (–æ–ø—Ü—ñ–π–Ω–æ)
const extractUserId = (req) => {
  if (req.user?.id) return req.user.id;
  if (req.body?.user_id) return req.body.user_id;
  return null;
};

// ‚ûï –°—Ç–≤–æ—Ä–∏—Ç–∏ —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è –∑ WebSocket
const createNotification = async (req, res) => {
  const io = req.app.get("io");
  const user_id = extractUserId(req);
  const message = req.body.message;

  console.log("üì• [POST /notification] –°—Ç–≤–æ—Ä–µ–Ω–Ω—è —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è:");
  console.log("üîê Headers:", req.headers);
  console.log("üë§ req.user:", req.user);
  console.log("üì¶ body:", req.body);

  if (!user_id || !message) {
    console.warn("‚ö†Ô∏è –í—ñ–¥—Å—É—Ç–Ω—ñ–π user_id –∞–±–æ message");
    return res.status(400).json({ message: "user_id —Ç–∞ message —î –æ–±–æ–≤‚Äô—è–∑–∫–æ–≤–∏–º–∏." });
  }

  try {
    const result = await pool.query(
      `INSERT INTO notifications (user_id, message) VALUES ($1, $2) RETURNING *`,
      [user_id, message]
    );
    const notification = result.rows[0];

    console.log("‚úÖ –°—Ç–≤–æ—Ä–µ–Ω–æ —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è:", notification);

    io.emit(`notification_${user_id}`, notification);
    io.emit("notification_all", notification);

    res.status(201).json(notification);
  } catch (error) {
    console.error("‚ùå SQL –ø–æ–º–∏–ª–∫–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ:", error);
    res.status(500).json({ message: "–ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ —Å—Ç–≤–æ—Ä–µ–Ω–Ω—ñ —Å–ø–æ–≤—ñ—â–µ–Ω–Ω—è.", error: error.message });
  }
};

